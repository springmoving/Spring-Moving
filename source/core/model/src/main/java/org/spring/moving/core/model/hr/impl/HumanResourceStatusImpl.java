package org.spring.moving.core.model.hr.impl;

import java.io.Serializable;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;
import org.spring.moving.core.domain.hr.api.HumanResourceStatus;

/**
 *
 * @author Brad Messerle
 */
@Entity
@Table(name="sm_human_resource_status")
public class HumanResourceStatusImpl implements HumanResourceStatus, Serializable {
    
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;
    private String name;

    @Override
    public Long getId() {
        return id;
    }

    @Override
    public void setId(Long id) {
        this.id = id;
    }

    @Override
    public String getName() {
        return name;
    }

    @Override
    public void setName(String name) {
        this.name = name;
    }
        
}
